class I18nManager {
    constructor() {
        this.currentLang = localStorage.getItem(I18N_CONFIG.storageKey) || I18N_CONFIG.defaultLanguage;
        // Ensure language is supported
        if (!I18N_CONFIG.languages.includes(this.currentLang)) {
            this.currentLang = I18N_CONFIG.defaultLanguage;
        }
    }

    setLanguage(lang) {
        if (I18N_CONFIG.languages.includes(lang)) {
            this.currentLang = lang;
            localStorage.setItem(I18N_CONFIG.storageKey, lang);
            this.translatePage();
            // Trigger custom event for other scripts (like search logic)
            window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
        }
    }

    t(key) {
        const translations = UI_TRANSLATIONS[this.currentLang] || UI_TRANSLATIONS[I18N_CONFIG.defaultLanguage];
        return translations[key] || key;
    }

    // Helper for objects in needs_config.js
    getText(obj) {
        if (!obj) return "";
        if (typeof obj === 'string') return obj;
        return obj[this.currentLang] || obj['fi'] || "";
    }

    translatePage() {
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const attrValue = el.getAttribute('data-i18n');
            
            // Support for [attr]key syntax
            const attrMatch = attrValue.match(/^\[([^\]]+)\](.*)$/);
            if (attrMatch) {
                const attrName = attrMatch[1];
                const key = attrMatch[2];
                el.setAttribute(attrName, this.t(key));
                return;
            }

            const key = attrValue;
            const translation = this.t(key);
            const hasHTML = translation.includes('<br>') || translation.includes('</span>') || translation.includes('</i>');
            
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                if (el.hasAttribute('placeholder')) el.placeholder = translation;
                else el.value = translation;
            } else if (el.children.length > 0 && !hasHTML) {
                // Element has child elements (e.g. <span>, <i> icons) –
                // only update the first plain text node to avoid destroying them
                let updated = false;
                for (const node of el.childNodes) {
                    if (node.nodeType === Node.TEXT_NODE && node.textContent.trim()) {
                        node.textContent = translation + ' ';
                        updated = true;
                        break;
                    }
                }
                // Fallback: if no text node found, prepend one
                if (!updated) {
                    el.insertBefore(document.createTextNode(translation + ' '), el.firstChild);
                }
            } else {
                if (hasHTML) {
                    el.innerHTML = translation;
                } else {
                    el.textContent = translation;
                }
            }
        });
        
        // Update language switcher active state
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.classList.toggle('active', btn.getAttribute('data-lang') === this.currentLang);
        });

        // Update html lang attribute
        document.documentElement.lang = this.currentLang;
    }
}

const i18n = new I18nManager();

// Run translation when DOM is loaded
document.addEventListener('DOMContentLoaded', () => {
    i18n.translatePage();
});

// Export to window
window.i18n = i18n;
